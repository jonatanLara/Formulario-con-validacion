            <h4 class=""><strong>About Campeche and Weather</h4></strong>
            <p class="justificado">Campeche, a Mexican state on the Yucatán Peninsula, spans limestone hills, rainforests and Gulf of Mexico beaches.
                Campeche-city is, without a doubt, one of the most beautiful capitals in the country. UNESCO named it as a model of a colonial baroque
                city planning. Additionally, it was recognized as a World Heritage Site. In the surroundings there are a large amount of Maya archaeological
                sites, out of which 16 are opened to the public, including Calakmul, the largest Maya city that has ever been discovered.
                <br>
