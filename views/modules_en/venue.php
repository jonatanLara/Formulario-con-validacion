            <br>
            <p class="justificado">The UNESCO Man and Biosphere reserve of Calakmul is one of the largest protected areas in Mexico, The area is home to
                5 of the 6 large cats that are native to Mexico. This includes its small but healthy population of jaguars as well as jaguarundis, ocelots,
                pumas and margays.
                <br>
            </p>
            <p class="justificado">Campeche is a place that will not only enable the discussion about natural and cultural sites, at the International
                Symposium, but it is the place of national heritage: old houses, churches and cathedrals, forts, warehouses, lighthouses, and among all
                these the extraordinary haciendas. Understandable, jointly with such a rich a unique intangible heritage, Campeche has its own people
                “los Campechanos”.
                <br>
            </p>
            <p class="justificado">The richness of the tropical forest biodiversity is converted into a large variety of unique local dishes, some might
                be very spicy. The local handcraft offers all types of products and, local dances with traditional dresses, are the best way to start the afternoon.
                The weather in September will be warm and nice (tropical forest weather). Participants are suggested to wear light casual clothes. Around midday
                the sun might hit severely, wearing a hat/cap is recommended.
