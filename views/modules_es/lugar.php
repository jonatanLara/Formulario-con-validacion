            <h4 class="">Acerca de Campeche y su clima</h4>
            <p class="justificado">La ciudad de Campeche, es la capital del Estado del mismo nombre. El estado de Campeche, está localizado en la
                península de Yucatán y cuenta con hermosas playas, una selva tropical abundante y una gran biodiversidad. El Centro Histórico de
                Campeche es sin duda alguna una de las capitales de estado más bonitas del país. UNESCO ha reconocido dicho centro histórico como
                Patrimonio Mundial de la Humanidad por ser un modelo de tipo colonial barroco. En los alrededores se encuentra una gran cantidad
                de sitios arqueológicos de la cultura maya, de los cuales 16 están abiertos al público y muchos más están siendo investigados por
                expertos nacionales y extranjeros.
                <br>
            <div class="flexbox center">
                <div class="video" >
                    <video style="width: 650px; height: auto" src="multimedia/Ciudad_Historica_Campeche.mp4" poster="image/campeche.jpg" controls></video>
                    <div class="flexbox flexbox-flex-end" style="color: #5b5b5b; font-size:9px;font-style: italic">(Desarrollo Económico, Turismo y Competitividad. H. Ayuntamiento Campeche)</div>
                </div>
            </div>
            <br>
            <p class="justificado">La reserva de la biosfera de Calakmul, doblemente inscrita en la UNESCO como reserva de biosfera y Patrimonio Mundial
                mixto, es una de las zonas protegidas más importantes del país. La selva de Calakmul alberga una gran variedad de especies animales y
                vegetales, de estas últimas se estima existen 1,600 especies de plantas, 10% endémicas. Calakmul representa la mayor reserva mexicana de
                bosque tropical, con una mezcla de selvas altas y medianas, con selvas bajas que llegan a inundarse en algunas temporadas por lo que
                existe una considerable cantidad de vegetación acuática. En cuanto a la fauna, contiene un importante número de especies amenazadas o
                en peligro de extinción; por ejemplo, cinco de las seis tipos de felinos conocidas en México: jaguar, puma, ocelote, tigrillo y leoncillo,
                habitan Calakmul.
                <br>
            </p>
            <p class="justificado">Campeche es sin duda uno de los mejores lugares para hablar de patrimonio en nuestro Simposio Internacional, ya que
                Campeche es un lugar patrimonial: casas antiguas, Iglesias hermosas, catedrales, fortificaciones, bodegas antiguas, faros y entre todo
                esto las maravillosas haciendas. Entendible, teniendo tal riqueza de patrimonio tangible, Campeche cuenta con uno de los patrimonios
                intangibles más variados. Campeche tiene a sus “campechanos” quienes como por arte de magia convierten la extraordinaria riqueza de la
                biodiversidad de la selva en  deliciosos manjares (platillos campechanos).
                <br>
            </p>
            <p class="justificado">Cuidado, algunos platillos pueden ser sumamente “picosos”. La artesanía local produce todo tipo de hermosos objetos y
                sus danzas con vestidos tradicionales, presentadas por ejemplo en la Plaza Central, es quizás la mejor forma de empezar una tarde memorable.
                En el mes de septiembre el clima es templado y agradable (clima del bosque tropical). Se sugiere a los participantes vestir ropas ligeras e
                informales. Alrededor de mediodía el sol puede ser muy fuerte, el uso de sombrero o gorra es recomendable.
                <br>
            </p>
